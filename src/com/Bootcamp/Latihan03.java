package com.Bootcamp;

public class Latihan03 {

	public static void main(String[] args) {
		int a = 1;
		a += 10; //asigment jumlah
		System.out.println("Nilai dari a = "+a);
		
		int b = 100;
		b -= 25; //asigment Pengurangan
		System.out.println("Nilai dari a = "+b);
		
		int c = 100;
		c *= 5; //asigment perkalian
		System.out.println("Nilai dari a = "+c);
		
		int d = 100;
		d /= 20; //asigment pembagian
		System.out.println("Nilai dari a = "+d);

	}

}
